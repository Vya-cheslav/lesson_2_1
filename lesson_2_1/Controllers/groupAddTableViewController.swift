    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "reuseIdentifier", for: indexPath) as! addGroupCell
        
        // Configure the cell...
        
        var arrayRows = [String]()
        
        if filter.count == 0 {
            
            arrayRows = strList
            
        }
        else {
           
            arrayRows = filter
            
        }
        
        cell.addGroupNameLabel.text = arrayRows[indexPath.row]
        cell.addGroupCountLable.text = "Участников группы: \(0)"
        cell.addGroupImageView.image = UIImage(named: arrayRows[indexPath.row])
        
        //
        return cell
    }
    
    func searchBar(_ searchBar: UISearchBar, textDidChange searchText: String) {
        if searchBar.text == nil || searchBar.text == "" {
            filter.removeAll()
        }
        else {
            filter = strList.filter({$0 == searchBar.text})
        }
        tableView.reloadData()
    }
    
